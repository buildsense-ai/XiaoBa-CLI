#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');

const keyId = process.env.CATSCO_SKILLHUB_ROOT_KEY_ID || process.argv[2];
const publicKeyPath = process.env.CATSCO_SKILLHUB_ROOT_PUBLIC_KEY_PATH || process.argv[3];

if (!keyId || !publicKeyPath) {
  console.error(`Usage:
  node scripts/update-skillhub-root-key.mjs <root-key-id> <root-public-key.pem>

or:
  CATSCO_SKILLHUB_ROOT_KEY_ID=catsco-root-prod-2026-01 \\
  CATSCO_SKILLHUB_ROOT_PUBLIC_KEY_PATH=/path/to/catsco-root-ed25519-public.pem \\
  node scripts/update-skillhub-root-key.mjs`);
  process.exit(1);
}

const publicKeyPem = fs.readFileSync(path.resolve(publicKeyPath), 'utf8').trim();
const target = path.join(rootDir, 'src', 'skillhub', 'trusted-keys.ts');
const escapedPem = publicKeyPem.replace(/`/g, '\\`');

const content = `export interface SkillHubTrustedRootKey {
  keyId: string;
  algorithm: 'ed25519';
  publicKeyPem: string;
}

/**
 * CatsCo SkillHub trust anchors.
 *
 * Generated by scripts/update-skillhub-root-key.mjs. The matching root private
 * key must stay on the SkillHub cloud side and must not be bundled in Agent.
 */
export const CATSCO_SKILLHUB_ROOT_PUBLIC_KEYS: SkillHubTrustedRootKey[] = [
  {
    keyId: ${JSON.stringify(keyId)},
    algorithm: 'ed25519',
    publicKeyPem: \`${escapedPem}\`,
  },
];
`;

fs.writeFileSync(target, content, 'utf8');
console.log(`Updated ${path.relative(rootDir, target)} with ${keyId}`);

